import java.util.Scanner;


public class threeLetterWords {

	public static void main(String[] args) {
		Scanner input=new Scanner(System.in);
		char[] symbols=input.next().toCharArray();
		for (int f=0;f<symbols.length;f++){
			String firstChar=Character.toString(symbols[f]);
			for(int s=0;s<symbols.length;s++){
				String secondChar=Character.toString(symbols[s]);
				for(int t=0; t<symbols.length;t++){
					String thirdChar=Character.toString(symbols[t]);
					System.out.print(firstChar+secondChar+thirdChar+" ");
				}
			}
		}
	}	
		
		
}
